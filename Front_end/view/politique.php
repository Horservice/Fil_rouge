POLITIQUE DE CONFIDENTIALITÉ

La présente Politique de Confidentialité définit la manière dont Futur Game collecte, utilise, conserve et divulgue les informations recueillies auprès des utilisateurs (ci-après désignés "Utilisateur") du site www.futurgame.com (ci-après désigné le "Site").

Collecte et utilisation des informations

Futur Game peut collecter des informations personnelles auprès des Utilisateurs de différentes manières, notamment lorsque les Utilisateurs visitent le Site, passent une commande, s'inscrivent sur le Site, remplissent un formulaire ou interagissent avec d'autres fonctionnalités disponibles sur le Site. Les informations personnelles collectées peuvent inclure le nom, l'adresse e-mail, l'adresse postale, le numéro de téléphone, les informations de paiement, entre autres.

Futur Game peut utiliser les informations personnelles collectées auprès des Utilisateurs pour les finalités suivantes :

Pour traiter les commandes et les transactions
Pour personnaliser l'expérience utilisateur et fournir un contenu adapté aux intérêts des Utilisateurs
Pour améliorer le Site et les services proposés par Futur Game
Pour envoyer des e-mails périodiques contenant des informations et des mises à jour concernant les commandes, ainsi que des actualités ou des offres promotionnelles relatives aux produits ou services de Futur Game
Pour répondre aux demandes, questions et préoccupations des Utilisateurs
Protection des informations

Futur Game met en place des mesures de sécurité appropriées pour protéger les informations personnelles des Utilisateurs contre tout accès non autorisé, altération, divulgation ou destruction.

Partage des informations personnelles

Futur Game ne vend, n'échange ni ne loue les informations personnelles des Utilisateurs à des tiers. Les informations personnelles des Utilisateurs ne seront divulguées qu'aux employés, sous-traitants, partenaires commerciaux et prestataires de services de Futur Game qui en ont besoin pour fournir des services en lien avec le Site et les activités de Futur Game, et qui ont accepté de ne pas divulguer ces informations à d'autres parties.

Cookies

Le Site peut utiliser des "cookies" pour améliorer l'expérience de navigation des Utilisateurs. Les Utilisateurs peuvent choisir de configurer leur navigateur web pour refuser les cookies ou pour être alertés lorsqu'un cookie est envoyé. Si les Utilisateurs désactivent les cookies, certaines fonctionnalités du Site peuvent ne pas fonctionner correctement.

Conservation des données

Futur Game conservera les informations personnelles des Utilisateurs aussi longtemps que nécessaire pour remplir les finalités pour lesquelles elles ont été collectées ou conformément aux exigences légales.

Droit d'accès et de rectification

Les Utilisateurs ont le droit d'accéder à leurs informations personnelles détenues par Futur Game et de demander leur rectification, leur mise à jour ou leur suppression.

Acceptation de la Politique de Confidentialité

En utilisant le Site, les Utilisateurs consentent à cette Politique de Confidentialité. Si les Utilisateurs n'acceptent pas cette Politique de Confidentialité, ils sont invités à ne pas utiliser le Site.

Modifications de la Politique de Confidentialité

Futur Game se réserve le droit de modifier à tout moment cette Politique de Confidentialité. Les modifications entrent en vigueur dès leur publication sur le Site.

